/** 파일 확장자 → 아이콘/타입 매핑 */
export function getFileIcon(fileName: string) {
  const ext = (fileName.split('.').pop() || '').toLowerCase();
  switch (ext) {
    case 'pdf': return { icon: '📄', type: 'PDF' };
    case 'pptx': case 'ppt': return { icon: '📊', type: 'PPT' };
    case 'zip': case 'rar': return { icon: '📦', type: 'ZIP' };
    case 'png': case 'jpg': case 'jpeg': case 'gif': return { icon: '🖼️', type: 'IMG' };
    default: return { icon: '📎', type: ext.toUpperCase() };
  }
}

/** 게시글 제목으로 첨부파일 매칭 (공백 무시) */
export function getAttachmentsForPost(title: string) {
  if (!title) return [];
  const normalize = (s: string): string => s.replace(/\s+/g, '').toLowerCase();
  const normalizedTitle = normalize(title);
  const matched = [];
  for (const entry of BOARD_ATTACHMENTS) {
    if (normalizedTitle.includes(normalize(entry.titleMatch))) {
      matched.push(...entry.files);
    }
  }
  return matched;
}

/** content에 저장된 첨부파일 마커 파싱 */
export function parseAttachmentMarker(content: string | null | undefined) {
  if (!content) return [];
  const match = content.match(/<!-- attachments: (.+?) -->/);
  if (!match) return [];
  return match[1].split(',').map(f => f.trim()).filter(Boolean);
}

/** 첨부파일 마커 생성 */
export function createAttachmentMarker(fileNames: string[] | null | undefined) {
  if (!fileNames || fileNames.length === 0) return '';
  return `<!-- attachments: ${fileNames.join(',')} -->`;
}

/** content에서 첨부파일 마커 제거 (표시용) */
export function stripAttachmentMarker(content: string | null | undefined) {
  if (!content) return '';
  return content.replace(/<!-- attachments: .+? -->/g, '').trim();
}

/** 사용 가능한 전체 파일 목록 (글쓰기에서 선택용) */
export function getAllAvailableFiles(): { name: string; path: string }[] {
  const allFiles: { name: string; path: string }[] = [];
  for (const entry of BOARD_ATTACHMENTS) {
    for (const file of entry.files) {
      if (!allFiles.find((f) => f.path === file.path)) {
        allFiles.push(file);
      }
    }
  }
  return allFiles;
}
